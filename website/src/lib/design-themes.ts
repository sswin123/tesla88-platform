/** Merge preset vars with custom overrides, return flat CSS var record */
export function resolveDesignVars(
  preset: string,
  overrides: Record<string, string> = {},
): Record<string, string> {
  const base = DESIGN_THEMES[preset]?.vars ?? DESIGN_THEMES.classic_purple.vars;
  return { ...base, ...overrides };
}
